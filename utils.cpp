#include "utils.hpp"

NS12_N::NS12_N()
{
	font = QFont();
	//font.setBold(false);  //declared after
	font.setCapitalization(QFont::Capitalization::MixedCase);
	font.setFamily("Noto Sans");
	font.setFixedPitch(false);
	//font.setHintingPreference(...);
	//font.setItalic(false);  //declared after
	//font.setKerning(...);
	//font.setLetterSpacing(...);
	font.setOverline(false);
	font.setPixelSize(12);  //size in px
	//font.setPointSize(...)  //size in pt
	font.setStretch(0);  //Sets the stretch factor for the font (150 = *1.5)
	font.setStrikeOut(false);
	font.setStyle(QFont::Style::StyleNormal);
	//font.setStyleHint(...);  //idk what is this thing...
	//font.setStyleName(...);  //will ignore style() & weight()
	//font.setStyleStrategy(...);  //will find a font family
	font.setUnderline(false);
	font.setWeight(QFont::Weight::Normal);
	//font.setWordSpacing(...);
}

